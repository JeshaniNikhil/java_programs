public class Main {
    public static void main(String[] args) {
        //loop
        //while
        int a=1;
        while(a<=10)
        {
            System.out.println(a);
            a++;
        }
        //for loop
        for (int i=1;i<10;i++)
        {
            System.out.println(i);
        }
        //do while loop
        do {
            System.out.println(a);
            a++;
        }while(a<=10);

    }
}