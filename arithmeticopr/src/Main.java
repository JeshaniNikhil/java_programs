public class Main {
    public static void main(String[] args) {
        //arithmetic opr
        double a=1;
        double b=2;
        double sum =a+b;
        double sub =a-b;
        double mul =a*b;
        double div =a/b;
        double modulo =a%b;
        System.out.println(sum);
        System.out.println(sub);
        System.out.println(mul);
        System.out.println(div);
        System.out.println(modulo);
        //unary opr
        int inc=1;
        System.out.println(inc--);
        System.out.println(inc);
    }
}